#!/usr/bin/env node
/**
 * Parses RESEND-DNS-RECORDS.json and generates NAMECHEAP-DNS-FIX.md
 * Run: node scripts/parse-dns-records.js
 */
'use strict';

const fs   = require('fs');
const path = require('path');

const INPUT  = path.join(__dirname, '..', 'RESEND-DNS-RECORDS.json');
const OUTPUT = path.join(__dirname, '..', 'NAMECHEAP-DNS-FIX.md');

if (!fs.existsSync(INPUT)) {
  console.error('❌ RESEND-DNS-RECORDS.json not found.');
  console.error('   Run PUSH-AND-FIX-RESEND.bat first to generate it.');
  process.exit(1);
}

let data;
try {
  data = JSON.parse(fs.readFileSync(INPUT, 'utf8'));
} catch (e) {
  console.error('❌ Could not parse RESEND-DNS-RECORDS.json:', e.message);
  process.exit(1);
}

const records = data.records || data.dns_records || [];
const domainName = data.name || 'mail.tokenia.live';

let md = `# NAMECHEAP DNS FIX — ${domainName}

**Goal:** Verify \`${domainName}\` in Resend so Tokenia can send emails.

> ✅ This ONLY adds records for the **subdomain** \`${domainName}\`.
> ✅ Your \`info@tokenia.live\` Outlook email is **NOT affected**.
> ✅ Your Private Email setup in Namecheap stays **100% intact**.

---

## Steps in Namecheap (3 minutes)

1. Go to **Namecheap → Domain List → tokenia.live → Manage**
2. Click **Advanced DNS** tab
3. Add the following records:

---

`;

if (records.length === 0) {
  // Fallback: show the raw JSON and explain the typical format
  md += `## Raw Response from Resend

\`\`\`json
${JSON.stringify(data, null, 2)}
\`\`\`

## Typical DNS Record Format for Resend

Based on Resend's standard configuration, you need 3 records:

| Type | Host (in Namecheap) | Value | TTL |
|------|---------------------|-------|-----|
| TXT | \`resend._domainkey.mail\` | \`p=MIGfMA0G...\` (from Resend dashboard) | Automatic |
| TXT | \`mail\` | \`v=spf1 include:amazonses.com ~all\` | Automatic |
| MX  | \`send.mail\` | \`feedback-smtp.us-east-1.amazonses.com\` | Automatic |

**To get exact values:** Log in at [resend.com](https://resend.com) → Domains → mail.tokenia.live
`;
} else {
  records.forEach((rec, i) => {
    const type     = rec.type || rec.record_type || 'TXT';
    const name     = rec.name || rec.host || '';
    const value    = rec.value || rec.content || '';
    const priority = rec.priority || rec.ttl_sec || '';

    // Convert full hostname to Namecheap "Host" field
    // Namecheap uses the subdomain part only (without .tokenia.live)
    let host = name
      .replace(/\.tokenia\.live\.?$/, '')
      .replace(/\.tokenia\.live$/, '');
    if (host === 'tokenia.live' || host === '') host = '@';

    md += `### Record ${i + 1} — ${type}

| Field | Value |
|-------|-------|
| **Type** | \`${type}\` |
| **Host** | \`${host}\` |
| **Value** | \`${value}\` |
${priority ? `| **Priority** | \`${priority}\` |\n` : ''}| **TTL** | Automatic |

`;
  });
}

md += `---

## After adding the records

1. Click **Save Changes** in Namecheap
2. Wait **30 minutes to 2 hours** for DNS propagation
3. Go to [resend.com/domains](https://resend.com/domains) → click **Verify** next to \`${domainName}\`
4. Once verified (green checkmark), emails will work

---

## What these records do

- **DKIM TXT** (\`resend._domainkey.mail\`) — proves Tokenia owns this domain; prevents spam filters
- **SPF TXT** — tells email providers Resend/Amazon SES is authorized to send from this domain
- **MX** (if present) — handles bounce/complaint feedback

---

## Does this affect my info@tokenia.live Outlook email?

**No.** These records are on the SUBDOMAIN \`mail.tokenia.live\`, not the root domain.
Your existing MX records for \`tokenia.live\` (Outlook/Namecheap Private Email) are untouched.

---

*Generated by Tokenia on ${new Date().toISOString().split('T')[0]}*
`;

fs.writeFileSync(OUTPUT, md);
console.log(`✅ NAMECHEAP-DNS-FIX.md generated (${records.length} records)`);
console.log(`   Open: ${OUTPUT}`);
